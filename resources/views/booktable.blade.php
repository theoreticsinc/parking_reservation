<script>
    function toggleBookingStatus(button) {
        if (button.classList.contains("cancel-btn")) {
            button.classList.remove("cancel-btn");
            button.classList.add("book-btn");
            button.innerHTML = "Book";
            button.onmouseover = function () {
                button.style.backgroundColor = "limegreen";
            };
            button.onmouseout = function () {
                button.style.backgroundColor = "#4caf50";
            };
        } else {
            button.classList.remove("book-btn");
            button.classList.add("cancel-btn");
            button.innerHTML = "Cancel Booking";
            button.onmouseover = function () {
                button.style.backgroundColor = "#ff3b3b";
            };
            button.onmouseout = function () {
                button.style.backgroundColor = "#ff3b3b";
            };
        }
    }
   function searchTable() {
        var input, filter, table, tr, td, i, txtValue;
        input = document.getElementById("searchInput");
        filter = input.value.toUpperCase();
        table = document.querySelector("table");
        tr = table.getElementsByTagName("tr");

        var found = false;

        for (i = 0; i < tr.length; i++) {
            td = tr[i].getElementsByTagName("td");
            for (var j = 0; j < td.length - 1; j++) { 
                txtValue = td[j].textContent || td[j].innerText;
                if (txtValue.toUpperCase().indexOf(filter) > -1) {
                    tr[i].style.background = "#f0d975"; 
                    found = true;
                    break; 
                } else {
                    tr[i].style.background = ""; 
                }
            }

            if (found) {
                tr[i].scrollIntoView({ behavior: 'smooth', block: 'center' });
                break; 
            }
        }
    }
